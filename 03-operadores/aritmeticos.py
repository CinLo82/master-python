#operadores aritméticos 
numero1 = 10
numero2 = 5 #operaador asignación =

suma = numero1 + numero2  #operador suma +
resta = numero1 - numero2  #operador resta -
multiplicacion = numero1 * numero2  #operador multiplicación *
division = numero1 / numero2  #operador división /
modulo = numero1 % numero2  #operador módulo %

print("************* CALCULADORA *****************")

print(f"La suma es: {suma}")
print(f"La resta es: {resta}")
print(f"La multiplicación es: {multiplicacion}")
print(f"La división es: {division}")
print(f"El módulo es: {modulo}")